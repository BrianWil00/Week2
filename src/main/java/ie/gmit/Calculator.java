package ie.gmit;

public class Calculator {
    public Calculator() {
    }

    public int add(int firstNum, int secondNum)
    {
        int total = firstNum + secondNum;
        return total;
    }

    public int subtract(int firstNum, int secondNum) {
        int result = firstNum - secondNum;
        return result;
    }
}
